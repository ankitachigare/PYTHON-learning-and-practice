# list slicing
list=[11,22,33,44,55,66,77,88,99]
print('element at 4 index : ',list[4])
print('element at 2 index : ',list[2])
print('element at last index is : ',list[-1])
print('element at second last index is : ',list[-2])
print('element at -8 index is : ',list[-8])


print(list[5:8])
print(list[3:])
print(list[:6])

print(list[-9:-1])
print(list[-6:])
print(list[:-1])